// Generate alternate chess piece sets as self-contained, per-set CSS files.
//
// chessground ships only the "cburnett" set (inline as data-URIs in
// chessground.cburnett.css, scoped globally under `.cg-wrap piece...`). To offer
// a choice of piece sets we download a handful of open-licensed sets from the
// Lichess (lila) asset repository, base64-inline their SVGs, and emit one CSS
// file per set with rules scoped under a wrapper class
// (`.pieces-<id> .cg-wrap piece...`).
//
// One file per set lets the client statically import only the default and
// lazy-load the rest on demand (see apps/web/src/styles/pieceSets.ts), so the
// initial bundle stays light. The OUTPUT (apps/web/src/styles/pieces/*.css) is
// committed, so the running app never needs network — this script only
// re-generates those assets.
//
// Usage:  node scripts/gen-piece-sets.mjs
import { writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const OUT_DIR = path.join(ROOT, 'apps/web/src/styles/pieces');
const BASE = 'https://raw.githubusercontent.com/lichess-org/lila/master/public/piece';

// Curated, visually distinct, open-licensed sets. The first is the default.
const SETS = ['cburnett', 'merida', 'alpha', 'maestro', 'california', 'cardinal', 'governor', 'horsey', 'staunty'];

const ROLE = { K: 'king', Q: 'queen', R: 'rook', B: 'bishop', N: 'knight', P: 'pawn' };
const COLOR = { w: 'white', b: 'black' };
const PIECES = [];
for (const c of ['w', 'b']) for (const r of ['K', 'Q', 'R', 'B', 'N', 'P']) PIECES.push(`${c}${r}`);

async function fetchSvg(set, piece) {
  const res = await fetch(`${BASE}/${set}/${piece}.svg`);
  if (!res.ok) throw new Error(`${set}/${piece}: HTTP ${res.status}`);
  return await res.text();
}

async function buildSet(set) {
  const rules = [];
  for (const piece of PIECES) {
    const svg = await fetchSvg(set, piece);
    const b64 = Buffer.from(svg.trim()).toString('base64');
    const role = ROLE[piece[1]];
    const color = COLOR[piece[0]];
    rules.push(
      `.pieces-${set} piece.${role}.${color} {\n` +
        `  background-image: url('data:image/svg+xml;base64,${b64}');\n}`,
    );
  }
  return rules.join('\n');
}

async function main() {
  mkdirSync(OUT_DIR, { recursive: true });
  const ok = [];
  for (const set of SETS) {
    try {
      process.stderr.write(`fetching ${set}…\n`);
      const rules = await buildSet(set);
      const header =
        '/* Generated by scripts/gen-piece-sets.mjs — do not edit by hand.\n' +
        ` * Piece set "${set}", art from the Lichess (lila) asset repository,\n` +
        ' * inlined as data-URIs and scoped under `.pieces-' +
        set +
        '` so the app needs no network.\n' +
        ' * Retains its upstream license (see lichess-org/lila COPYING.md).\n' +
        ' */\n';
      writeFileSync(path.join(OUT_DIR, `${set}.css`), `${header}\n${rules}\n`);
      ok.push(set);
    } catch (e) {
      process.stderr.write(`  skipped ${set}: ${e.message}\n`);
    }
  }
  process.stderr.write(`\nWrote ${ok.length} sets → ${path.relative(ROOT, OUT_DIR)}/\n`);
  // Emit the id list so the client catalogue can be kept in sync by eye.
  process.stdout.write(JSON.stringify(ok) + '\n');
}

main().catch((e) => {
  process.stderr.write(`error: ${e.stack ?? e}\n`);
  process.exit(1);
});
