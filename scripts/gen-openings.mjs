// Generate the bundled ECO opening database for offline opening-name lookup.
//
// Source: the lichess-org/chess-openings project (CC0) — five TSVs (a–e) of
// `eco<TAB>name<TAB>pgn`. We replay each line with chess.js to capture the
// position's EPD (board + side-to-move + castling + en-passant), which is the
// key used to name an arbitrary board position (transposition-aware). The SAN
// move list is kept so the client can load any opening straight onto the board.
//
// The OUTPUT (apps/web/src/data/openings-eco.ts) is committed, so the running
// app needs no network — this script only re-generates that dataset.
//
// Usage:  node scripts/gen-openings.mjs
import { writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';
import path from 'node:path';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const OUT_DIR = path.join(ROOT, 'apps/web/src/data');
const OUT = path.join(OUT_DIR, 'openings-eco.ts');
const BASE = 'https://raw.githubusercontent.com/lichess-org/chess-openings/master';

// chess.js lives in the workspace packages, not next to this script.
const require = createRequire(path.join(ROOT, 'apps/server/package.json'));
const { Chess } = require('chess.js');

const epdOf = (fen) => fen.split(' ').slice(0, 4).join(' ');

async function fetchTsv(file) {
  const res = await fetch(`${BASE}/${file}`);
  if (!res.ok) throw new Error(`${file}: HTTP ${res.status}`);
  return await res.text();
}

async function main() {
  const seen = new Set();
  const rows = [];
  for (const file of ['a.tsv', 'b.tsv', 'c.tsv', 'd.tsv', 'e.tsv']) {
    process.stderr.write(`fetching ${file}…\n`);
    const text = await fetchTsv(file);
    for (const line of text.split('\n').slice(1)) {
      if (!line.trim()) continue;
      const [eco, name, pgn] = line.split('\t');
      if (!eco || !name || !pgn) continue;
      const c = new Chess();
      try {
        c.loadPgn(pgn);
      } catch {
        continue;
      }
      const san = c.history();
      if (san.length === 0) continue;
      const epd = epdOf(c.fen());
      if (seen.has(epd)) continue; // keep the first (shortest) naming of a position
      seen.add(epd);
      rows.push([eco, name, epd, san.join(' ')]);
    }
  }

  rows.sort((a, b) => (a[0] < b[0] ? -1 : a[0] > b[0] ? 1 : a[1].localeCompare(b[1])));

  const body = rows.map((r) => '  ' + JSON.stringify(r) + ',').join('\n');
  const out =
    '// Generated by scripts/gen-openings.mjs — do not edit by hand.\n' +
    '//\n' +
    '// Bundled ECO opening database (lichess-org/chess-openings, CC0).\n' +
    `// ${rows.length} named positions. Tuple = [eco, name, epd, sanMoves(space-joined)].\n` +
    '// `epd` is the FEN sans move counters, used to name any board position.\n' +
    '\n' +
    'export type EcoTuple = [eco: string, name: string, epd: string, san: string];\n' +
    '\n' +
    `export const ECO_OPENINGS: EcoTuple[] = [\n${body}\n];\n`;

  mkdirSync(OUT_DIR, { recursive: true });
  writeFileSync(OUT, out);
  process.stderr.write(`\nWrote ${rows.length} openings → ${path.relative(ROOT, OUT)}\n`);
}

main().catch((e) => {
  process.stderr.write(`error: ${e.stack ?? e}\n`);
  process.exit(1);
});
